package HomeWork;

public class Exercise_11 {
    public static void main(String[] args) {
        double degrees1 = 30;
        double radians1 = Math.toRadians(degrees1);
        System.out.format("Синус 30 градусов равен " + Math.sin(radians1));
        System.out.format("Косинус 30 градусов равен " + Math.cos(radians1));
        System.out.format("Тенгенс 30 градусов равен " + Math.tan(radians1));
    }
}
